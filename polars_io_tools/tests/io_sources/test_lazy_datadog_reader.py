def test_interval_parameter(mock_metric_query):
    """Test that the dd_interval parameter is passed to metric_query."""
    query = "some:query"
    custom_interval = 300
    start_dt = datetime.datetime(2023, 1, 1)
    end_dt = datetime.datetime(2023, 1, 2)

    mock_metric_query.return_value = {"series": []}

    lf = cpl.scan_datadog(query=query, api_key=DUMMY_API_KEY, app_key=DUMMY_APP_KEY, dd_interval=custom_interval).filter(
        (pl.col("timestamp") >= start_dt) & (pl.col("timestamp") < end_dt)
    )
    lf.collect()

    mock_metric_query.assert_called_once_with(
        query=query,
        start=int(start_dt.timestamp()),
        end=int(end_dt.timestamp()),
        api_key=DUMMY_API_KEY,
        app_key=DUMMY_APP_KEY,
        interval=custom_interval,
    )


def test_chunking(mock_metric_query):
    """Test that API calls are chunked based on max_chunk_duration_seconds."""
    query = "my.chunked.metric"
    # 2 hours range, 1 hour chunk duration -> 2 calls
    start_dt = datetime.datetime(2023, 1, 1, 0, 0, 0, tzinfo=datetime.timezone.utc)
    end_dt = datetime.datetime(2023, 1, 1, 2, 0, 0, tzinfo=datetime.timezone.utc)
    max_chunk_sec = 3600

    mock_response_chunk = {
        "series": [
            {"metric": "my.chunked.metric", "pointlist": [[start_dt.timestamp() * 1000 + i * 1000, float(i)] for i in range(2)], "tag_set": []}
        ]
    }
    mock_metric_query.return_value = mock_response_chunk

    lf = cpl.scan_datadog(query=query, api_key=DUMMY_API_KEY, app_key=DUMMY_APP_KEY, max_chunk_duration_seconds=max_chunk_sec).filter(
        (pl.col("timestamp") >= start_dt.replace(tzinfo=None)) & (pl.col("timestamp") < end_dt.replace(tzinfo=None))
    )
    df = lf.collect()

    assert mock_metric_query.call_count == 2
    assert len(df) == 4


# These tests verify that scan_datadog LazyFrames can be serialized with cloudpickle,
# which is required for distributed computing (e.g., Ray). Tests are located here
# rather than in test_pickle.py because they require Datadog API mocking infrastructure.


class TestScanDatadogPickle:
    """Tests for scan_datadog cloudpickle serialization support."""

    def test_scan_datadog_pickle_basic(self, mock_metric_query):
        """scan_datadog LazyFrames can be pickled and unpickled."""
        import cloudpickle

        query = "avg:my.metric{tag:value}"
        start_dt = datetime.datetime(2023, 1, 1, 0, 0, 0)
        end_dt = datetime.datetime(2023, 1, 1, 1, 0, 0)

        mock_response = {
            "series": [
                {
                    "metric": "my.metric",
                    "pointlist": [[start_dt.timestamp() * 1000, 1.0], [(start_dt.timestamp() + 1800) * 1000, 2.0]],
                    "tag_set": ["tag:value"],
                }
            ]
        }
        mock_metric_query.return_value = mock_response

        lf = cpl.scan_datadog(query=query, api_key=DUMMY_API_KEY, app_key=DUMMY_APP_KEY).filter(
            (pl.col("timestamp") >= start_dt) & (pl.col("timestamp") < end_dt)
        )

        # Pickle roundtrip
        pickled = cloudpickle.dumps(lf)
        lf_unpickled = cloudpickle.loads(pickled)

        # Verify schema is preserved
        assert lf.collect_schema() == lf_unpickled.collect_schema()

        # Verify data matches (both collected within mock context)
        expected = lf.sort("timestamp").collect()
        result = lf_unpickled.sort("timestamp").collect()
        assert_frame_equal(expected, result)

    def test_scan_datadog_pickle_with_filter(self, mock_metric_query):
        """scan_datadog with filters can be pickled."""
        import cloudpickle

        query = "avg:my.metric{tag:value}"
        start_dt = datetime.datetime(2023, 1, 1, 0, 0, 0)
        end_dt = datetime.datetime(2023, 1, 1, 1, 0, 0)

        mock_response = {
            "series": [
                {
                    "metric": "my.metric",
                    "pointlist": [
                        [start_dt.timestamp() * 1000, 1.0],
                        [(start_dt.timestamp() + 1800) * 1000, 5.0],
                    ],
                    "tag_set": ["tag:value"],
                }
            ]
        }
        mock_metric_query.return_value = mock_response

        lf = (
            cpl.scan_datadog(query=query, api_key=DUMMY_API_KEY, app_key=DUMMY_APP_KEY)
            .filter((pl.col("timestamp") >= start_dt) & (pl.col("timestamp") < end_dt))
            .filter(pl.col("value") > 3.0)
        )

        pickled = cloudpickle.dumps(lf)
        lf_unpickled = cloudpickle.loads(pickled)

        # Verify schema is preserved
        assert lf.collect_schema() == lf_unpickled.collect_schema()

        # Verify data matches
        expected = lf.sort("timestamp").collect()
        result = lf_unpickled.sort("timestamp").collect()
        assert_frame_equal(expected, result)
