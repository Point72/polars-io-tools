def test_no_lower_bound(mock_metric_query):
    """Test that we raise if no lower time bound is provided."""
    query = "a:query"
    with pytest.raises(ComputeError):
        cpl.scan_datadog(query=query, api_key=DUMMY_API_KEY, app_key=DUMMY_APP_KEY).filter(
            pl.col("timestamp") < datetime.datetime(2025, 1, 1)
        ).collect()
    mock_metric_query.assert_not_called()

    with pytest.raises(ComputeError):
        cpl.scan_datadog(query=query, api_key=DUMMY_API_KEY, app_key=DUMMY_APP_KEY).collect()
    mock_metric_query.assert_not_called()


def test_start_after_end(mock_metric_query):
    """Test that we raise if start time is after end time."""
    query = "a:query"
    start_dt = datetime.datetime(2023, 1, 2, tzinfo=datetime.timezone.utc)
    end_dt = datetime.datetime(2023, 1, 1, tzinfo=datetime.timezone.utc)

    df = (
        cpl.scan_datadog(query=query, api_key=DUMMY_API_KEY, app_key=DUMMY_APP_KEY)
        .filter((pl.col("timestamp") >= start_dt) & (pl.col("timestamp") < end_dt))
        .collect()
    )
    assert df.is_empty()

    second_df = (
        cpl.scan_datadog(query=query, api_key=DUMMY_API_KEY, app_key=DUMMY_APP_KEY)
        .filter((pl.col("timestamp") >= start_dt) & (pl.col("timestamp") < end_dt))
        .select(["timestamp", "value"])
        .collect()
    )
    assert second_df.is_empty()
    assert second_df.shape == (0, 2)

    mock_metric_query.assert_not_called()


